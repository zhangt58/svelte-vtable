                    id="filter-{column.key}-to"
                    type={inputType}
                    class="flex-1 min-w-0 px-2 py-1 text-xs border border-gray-200 dark:border-gray-600 rounded bg-white dark:bg-gray-800 text-gray-700 dark:text-gray-200 focus:outline-none focus:border-blue-500 dark:focus:border-blue-400"
                    value={dateRange.to || ''}
                    oninput={(e) => handleDateRangeChange(column.key, 'to', e.currentTarget.value)}
                  />
                  {#if column.maxValue}
                    <button
                      type="button"
                      class="shrink-0 px-2 py-0.5 text-xs rounded border border-indigo-400 text-indigo-700 dark:text-indigo-300 hover:bg-indigo-50 dark:hover:bg-indigo-900/20 transition-colors flex items-center"
                      title="Set To to the latest record ({column.maxValue})"
                      aria-label="Set To to the latest record"
                      onclick={() => handleDateRangeChange(column.key, 'to', column.maxValue)}
                    >
                      <!-- Double-chevron-right icon (Latest) -->
                      <svg
                        class="w-4 h-4 text-indigo-700 dark:text-indigo-300"
                        viewBox="0 0 24 24"
                        fill="none"
                        aria-hidden="true"
                      >
                        <path
                          d="M12.5 7.5L17 12l-4.5 4.5"
                          stroke="currentColor"
                          stroke-width="2"
                          stroke-linecap="round"
                          stroke-linejoin="round"
                          fill="none"
                        />
                        <path
                          d="M6.5 7.5L11 12l-4.5 4.5"
                          stroke="currentColor"
                          stroke-width="2"
                          stroke-linecap="round"
                          stroke-linejoin="round"
                          fill="none"
                        />
                      </svg>
                    </button>
                  {/if}
                </div>

                <!-- Dual-range slider (only when column provides min/max) -->
                {#if column.minValue && column.maxValue && totalMs > 0}
                  <div class="dual-range-wrapper" aria-label="Date range slider">
                    <!-- Background track -->
                    <div class="dual-range-track-bg"></div>
                    <!-- Active-range highlight -->
                    <div
                      class="dual-range-track-fill"
                      style="left:{fromPct}%; right:{100 - toPct}%"
                    ></div>
                    <!-- From thumb -->
                    <input
                      type="range"
                      class="dual-range-input"
                      min="0"
                      max="100"
                      step="0.02"
                      value={fromPct}
                      style="z-index:{fromPct >= toPct - 2 ? 4 : 3}"
                      oninput={(e) =>
                        handleFromSlider(
                          column.key,
                          parseFloat(e.currentTarget.value),
                          minMs,
                          maxMs,
                          inputType,
                        )}
                      aria-label="Range start"
                    />
                    <!-- To thumb -->
                    <input
                      type="range"
                      class="dual-range-input"
                      min="0"
                      max="100"
                      step="0.02"
                      value={toPct}
                      style="z-index:{fromPct >= toPct - 2 ? 3 : 4}"
                      oninput={(e) =>
                        handleToSlider(
                          column.key,
                          parseFloat(e.currentTarget.value),
                          minMs,
                          maxMs,
                          inputType,
                        )}
                      aria-label="Range end"
                    />
                  </div>
                {/if}

                <!-- Relative-range quick buttons -->
                {#if applicablePresets.length > 0}
                  <div
                    class="flex flex-wrap gap-1 pt-1 border-t border-gray-100 dark:border-gray-600"
                  >
                    {#each applicablePresets as preset (preset.label)}
                      <button
                        type="button"
                        class="px-2 py-0.5 text-xs rounded border border-blue-400 text-blue-700 dark:text-blue-300 hover:bg-blue-50 dark:hover:bg-blue-900/20 transition-colors"
                        title="Last {preset.value} {preset.unit}{preset.value !== 1 ? 's' : ''}"
                        onclick={() => setRelativeRange(column.key, preset.value, preset.unit)}
                      >
                        Last {preset.label}
                      </button>
                    {/each}
                  </div>
                {/if}

                {#if dateRange.from || dateRange.to}
                  <button
                    type="button"
                    class="self-start px-2 py-0.5 text-xs text-red-600 border border-red-400 rounded hover:bg-red-50 dark:hover:bg-red-900/20 transition-colors"
                    onclick={() => clearColumn(column.key)}
                  >
                    Clear range
                  </button>
                {/if}
              </div>
            {:else}
              <!-- Value-selection filter UI (checkboxes) -->
              <div class="px-1 py-1 border-b border-gray-200 dark:border-gray-600">
                <div class="flex items-center gap-1 w-full min-w-0">
                  <input
                    type="text"
                    placeholder="Filter..."
                    class="p-1 flex-1 min-w-0 px-2 py-1 text-xs border border-gray-200 dark:border-gray-600 rounded bg-white dark:bg-gray-700 text-gray-700 dark:text-gray-200 focus:outline-none focus:border-blue-500"
                    bind:value={searchQueries[column.key]}
                  />

                  <div class="flex items-center gap-1">
                    <button
                      class="p-0.5 flex-1 flex items-center justify-center gap-1 text-sm text-gray-600 dark:text-gray-400 bg-transparent border border-gray-200 dark:border-gray-700 rounded hover:bg-gray-100 dark:hover:bg-gray-700 hover:border-gray-300 dark:hover:border-gray-600 hover:text-gray-700 dark:hover:text-gray-300 transition-all"
                      class:bg-blue-100={currentSortMode === 'name'}
                      class:dark:bg-blue-900={currentSortMode === 'name'}
                      onclick={() => toggleSortMode(column.key, 'name')}
                      title="Sort by name"
                      type="button"
                    >
                      <span
                        class="flex flex-col items-center leading-none font-semibold"
                        style="font-size:0.6rem;"
                      >
                        <span>A</span>
                        <span>Z</span>
                      </span>
                      <ArrowDownOutline
                        class={'w-3 h-3 transition-transform ' +
                          (currentSortMode === 'name'
                            ? 'text-blue-900 dark:text-blue-100'
                            : 'text-gray-400 dark:text-gray-500')}
                        style={currentSortMode === 'name' && currentSortDir === 'asc'
                          ? 'transform: rotate(180deg);'
                          : ''}
                        aria-hidden="true"
                      />
                    </button>

                    <button
                      class="p-0.5 flex-1 flex items-center justify-center gap-1 text-sm text-gray-600 dark:text-gray-400 bg-transparent border border-gray-200 dark:border-gray-700 rounded hover:bg-gray-100 dark:hover:bg-gray-700 hover:border-gray-300 dark:hover:border-gray-600 hover:text-gray-700 dark:hover:text-gray-300 transition-all"
                      class:bg-blue-100={currentSortMode === 'number'}
                      class:dark:bg-blue-900={currentSortMode === 'number'}
                      onclick={() => toggleSortMode(column.key, 'number')}
                      title="Sort by number"
                      aria-label="Sort by number"
                      type="button"
                    >
                      <span
                        class="flex flex-col items-center leading-none font-semibold"
                        style="font-size:0.6rem;"
                      >
                        <span>0</span>
                        <span>9</span>
                      </span>
                      <ArrowDownOutline
                        class={'w-3 h-3 transition-transform ' +
                          (currentSortMode === 'number'
                            ? 'text-blue-900 dark:text-blue-100'
                            : 'text-gray-400 dark:text-gray-500')}
                        style={currentSortMode === 'number' && currentSortDir === 'asc'
                          ? 'transform: rotate(180deg);'
                          : ''}
                        aria-hidden="true"
                      />
                    </button>

                    {#if showCounts && column.counts}
                      <button
                        class="p-0.5 flex-1 flex items-center justify-center gap-1 text-sm text-gray-600 dark:text-gray-400 bg-transparent border border-gray-200 dark:border-gray-700 rounded hover:bg-gray-100 dark:hover:bg-gray-700 hover:border-gray-300 dark:hover:border-gray-600 hover:text-gray-700 dark:hover:text-gray-300 transition-all"
                        class:bg-blue-100={currentSortMode === 'count'}
                        class:dark:bg-blue-900={currentSortMode === 'count'}
                        onclick={() => toggleSortMode(column.key, 'count')}
                        title="Sort by count"
                        type="button"
                      >
                        <span class="text-sm font-semibold">#</span>
                        <ArrowDownOutline
                          class={'w-3 h-3 transition-transform ' +
                            (currentSortMode === 'count'
                              ? 'text-blue-900 dark:text-blue-100'
                              : 'text-gray-400 dark:text-gray-500')}
                          style={currentSortMode === 'count' && currentSortDir === 'asc'
                            ? 'transform: rotate(180deg);'
                            : ''}
                          aria-hidden="true"
                        />
                      </button>
                    {/if}
                  </div>
                </div>
              </div>

              <!-- Action row: Check all / Invert / Uncheck all (icon buttons) -->
              <div class="px-2 py-1 border-b border-gray-100 dark:border-gray-600">
                <div class="flex items-center justify-between">
                  <div class="flex items-center gap-1">
                    <button
                      type="button"
                      class="p-1 rounded-md flex items-center gap-1 text-gray-600 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-600 disabled:opacity-50 disabled:cursor-not-allowed"
                      onclick={() => checkAll(column.key, sortedValues)}
                      disabled={sortedValues.length === 0}
                      aria-label="Check all"
                      title="Check all"
                    >
                      <!-- Checked square icon (higher contrast) -->
                      <svg
                        class="w-4 h-4 text-gray-800 dark:text-white"
                        viewBox="0 0 24 24"
                        fill="none"
                        aria-hidden="true"
                      >
                        <rect
                          x="3"
                          y="3"
                          width="18"
                          height="18"
                          rx="2"
                          stroke="currentColor"
                          stroke-width="2.5"
                          fill="none"
                        />
                        <path
                          d="M7 13l3 3 7-7"
                          stroke="currentColor"
                          stroke-width="2"
                          stroke-linecap="round"
                          stroke-linejoin="round"
                          fill="none"
                        />
                      </svg>
                      <span class="text-sm text-gray-800 dark:text-gray-100 select-none">All</span>
